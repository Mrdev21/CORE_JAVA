public class Unary {
    public static void main(String[] args) {

        int x = 5;
        int y = -x;
        System.out.println(y);

        int a = 5;
        a +=1;
        System.out.println(a);
        a += 1;
        System.out.println(a);
        a++;
        System.out.println(a);
        ++a;
        System.out.println(a);

        int b = 10;

        System.out.println(--b);
        System.out.println(b);

        System.out.println(b--);
        System.out.println(b);
    }
}
