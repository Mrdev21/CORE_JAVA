public class Arithmetic {
    public static void main(String[] args) {
        System.out.println("Adding divide multi modulo and other");

        int a = 8;
        int b = 5;

        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a%b);  //Modulus operator returns only reminder

    }
}
